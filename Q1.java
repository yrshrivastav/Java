public class Q1 {
	public static void main(String[] args) {
	
		// 1- int to float
		int a = 25;
		System.out.println("The integer value is : "+a);
		
		float b = a;
		System.out.println("The float value is : "+b);
		
		// 2- float to double
		float c = 13.54f;
		System.out.println("The float value is : "+c);
		
		double d = c;
		System.out.println("The double value is : "+d);
		
		// 3- long to byte
		long e = 2465;
		System.out.println("The long value is : "+e);
		
		byte f = (byte)e;
		System.out.println("The byte value is : "+f);
		
		// 4- short to double
		short g = 12;
		System.out.println("The short value is : "+g);
		
		double h = g;
		System.out.println("The double value is : "+h);
		
	}
}